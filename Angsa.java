package polymorphismunggas;
public class Angsa extends PolymorphismUnggas {
	
   @Override
        void makan() {
        System.out.println("Angsa Makan");
   }
 
   @Override
        void lari() {
        System.out.println("Angsa Lari");
   } 

   @Override
        void berbulu() {
        System.out.println("Angsa Berbulu");
   } 

   @Override
        void bertelur() {
        System.out.println("Angsa Bertelur");
   } 
    public void berenang(){
        System.out.println("Merpati Berenang");

   }
}
